/* USER CODE BEGIN EV */
static void hard_fault_handler_c(uint32_t *stack_frame)
{
    uint32_t r0  = stack_frame[0];
    uint32_t r1  = stack_frame[1];
    uint32_t r2  = stack_frame[2];
    uint32_t r3  = stack_frame[3];
    uint32_t r12 = stack_frame[4];
    uint32_t lr  = stack_frame[5];
    uint32_t pc  = stack_frame[6];
    uint32_t psr = stack_frame[7];

    uint32_t cfsr = SCB->CFSR;
    uint32_t hfsr = SCB->HFSR;
    uint32_t mmfar = SCB->MMFAR;
    uint32_t bfar = SCB->BFAR;

    printf("\n***** HARDFAULT *****\n");
    printf("R0  = 0x%08lX\n", (unsigned long)r0);
    printf("R1  = 0x%08lX\n", (unsigned long)r1);
    printf("R2  = 0x%08lX\n", (unsigned long)r2);
    printf("R3  = 0x%08lX\n", (unsigned long)r3);
    printf("R12 = 0x%08lX\n", (unsigned long)r12);
    printf("LR  = 0x%08lX\n", (unsigned long)lr);
    printf("PC  = 0x%08lX\n", (unsigned long)pc);
    printf("PSR = 0x%08lX\n", (unsigned long)psr);
    printf("CFSR= 0x%08lX HFSR=0x%08lX\n", (unsigned long)cfsr, (unsigned long)hfsr);
    printf("MMFAR=0x%08lX BFAR=0x%08lX\n", (unsigned long)mmfar, (unsigned long)bfar);
    fflush(stdout);

    /* Optionally loop here to inspect CPU state with debugger */
    while(1);
}
/* USER CODE END EV */

